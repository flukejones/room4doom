impl Default for SfxName {
    fn default() -> Self {
        Self::None
    }
}

impl From<u8> for SfxName {
    fn from(i: u8) -> Self {
        if i >= SfxName::NumSfx as u8 {
            panic!("{} is not a variant of SfxName", i);
        }
        unsafe { std::mem::transmute(i) }
    }
}
